	private void btnSubmitActionPerformed(ActionEvent evt) {
		JOptionPane.showMessageDialog(null, "Successfully added Rule!");
		int id = Integer.parseInt(txtImmunizationID.getText());
		int age1 = Integer.parseInt(txtAgeFrom.getText());
		int age2 = Integer.parseInt(txtAgeTo.getText());
		int re = Integer.parseInt(txtRequiredAmount.getText());
		String descr = "New Rule";
		for (ImmunizationRule x : d.getImmunizationRules()) {
			if (x.getRuleID() == id) {
				descr = x.getImmunization();
			}
		}
		ImmunizationRule r = new ImmunizationRule(id, age1, age2, descr, re);
		d.addImmunizationRule(r);
		try {
			d.getWriter().writeImmunizationRuleData();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}

		f.setVisible(false);
		f.dispose();

		
	}

